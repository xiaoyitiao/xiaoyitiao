/**
 * 检查新药品与已有药品是否存在已知相互作用
 * @param {string} 新药品名称
 * @param {string[]} 已有药品名称列表
 * @returns {{有风险: boolean, 警告: string}}
 */
export function 检查药品相互作用(新药品名称, 已有药品名称列表) {
  if (!新药品名称 || !已有药品名称列表 || 已有药品名称列表.length === 0) {
    return { 有风险: false, 警告: '' };
  }

  for (const 组合 of 禁忌组合) {
    const 命中药品 = 组合.药品.filter(名称 =>
      新药品名称.includes(名称) || 已有药品名称列表.some(已有 => 已有.includes(名称))
    );

    // 必须同时命中组合中的两种药品才触发警告
    if (命中药品.length >= 2) {
      return { 有风险: true, 警告: 组合.警告 };
    }
  }

  return { 有风险: false, 警告: '' };
}

/**
 * 检查单个药品列表内部是否存在相互作用
 * @param {string[]} 药品名称列表
 * @returns {{有风险: boolean, 警告: string}}
 */
export function 检查列表相互作用(药品名称列表) {
  if (!药品名称列表 || 药品名称列表.length < 2) {
    return { 有风险: false, 警告: '' };
  }

  for (const 组合 of 禁忌组合) {
    const 命中药品 = 组合.药品.filter(名称 =>
      药品名称列表.some(已有 => 已有.includes(名称))
    );

    if (命中药品.length >= 2) {
      return { 有风险: true, 警告: 组合.警告 };
    }
  }

  return { 有风险: false, 警告: '' };
}
