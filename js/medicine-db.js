/**
 * 根据文字内容模糊匹配本地药品数据库
 * @param {string} 识别文字
 * @returns {Object|null} 匹配到的药品信息
 */
export function 匹配药品(识别文字) {
  if (!识别文字) return null;

  const 文本 = 识别文字.toLowerCase();

  for (const 药品 of 药品数据库) {
    // 先匹配主名称
    if (文本.includes(药品.名称.toLowerCase())) {
      return 药品;
    }
    // 再匹配别名
    for (const 别名 of 药品.别名) {
      if (文本.includes(别名.toLowerCase())) {
        return 药品;
      }
    }
  }

  return null;
}
